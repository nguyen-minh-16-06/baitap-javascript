let a = 5, b = 2;

let tong = a + b; // Tổng = 7
let hieu = a - b; // Hiệu = 3
let tich = a * b; // Tích = 10
let thuong = a / b; // Thương = 2.5
let soDu = a % b; // Dư = 1

// Xuất kết quả
console.log("Tổng của 2 số: " + tong);
console.log("Hiệu của 2 số: " + hieu);
console.log("Tích của 2 số: " + tich);
console.log("Thương của 2 số: " + thuong);
console.log("Phép chia lấy dư của a chia cho b: " + soDu);

